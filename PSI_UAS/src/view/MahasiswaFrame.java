    public void populateDataToTable() throws SQLException{
      
        List<Mahasiswa> mhs = mahasiswaController.loadMahasiswas();
        
        for(Mahasiswa mh : mhs){
            Object[] row = new Object[3];
            row[0] = mh.getNrp();
            row[1] = mh.getNama();
            row[2] = mh.getJenisKelamin();
            
            model.addRow(row);
        }
    }
    public void refreshTable() throws SQLException{
        model.setRowCount(0);
        populateDataToTable();
    }
    
