    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(validasiInput()) {
            try {
                mahasiswa.setNrp(tfNrp.getText().trim());
                mahasiswa.setNama(tfNama.getText().trim());
                mahasiswa.setJenisKelamin(cbJenisKelamin.getSelectedItem().toString());
                
                if(mahasiswaController.simpanMahasiswa()) {
                    JOptionPane.showMessageDialog(null, "Tambah mahasiswa sukses");
                    refreshTable();
                } else {
                    JOptionPane.showMessageDialog(null, "Tambah mahasiswa gagal");
                }
            } catch (SQLException ex) {
                Logger.getLogger(MahasiswaFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak lengkap");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

